import fs from 'node:fs/promises';
import path from 'node:path';

const repoRoot = path.resolve(new URL('..', import.meta.url).pathname);
const catalogPath = path.join(repoRoot, 'content/catalog.json');
const outputPath = path.join(repoRoot, 'docs/THIRD_PARTY_CONTENT_AUDIT.generated.md');
const contentExtensions = new Set([
  '.milk',
  '.milk2',
  '.shape',
  '.wave',
  '.png',
  '.jpg',
  '.jpeg',
  '.webp',
  '.bmp',
  '.jfif',
  '.frag',
  '.vert',
  '.glsl',
  '.hlsl',
  '.ini',
]);
const licenseNamePattern = /^(license|licenses|copying|copyright|notice)(\..*)?$/i;
const isContentJson = (relativePath) => /(^|\/)(presets\/converted|fragments|plugins)\//.test(relativePath)
  || /(^|\/)manifest\.json$/.test(relativePath);

const args = process.argv.slice(2);
const write = args.includes('--write');
const scanRoots = args.filter((arg) => arg !== '--write');

const shouldSkipDir = (name) => [
  '.git',
  'node_modules',
  'target',
  'pkg',
  'dist',
  'build',
  '.next',
  '.vite',
].includes(name);

const readJson = async (file) => JSON.parse(await fs.readFile(file, 'utf8'));

const walk = async (root, stats) => {
  let entries;
  try {
    entries = await fs.readdir(root, { withFileTypes: true });
  } catch {
    return;
  }

  for (const entry of entries) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      if (!shouldSkipDir(entry.name)) await walk(fullPath, stats);
      continue;
    }
    if (!entry.isFile()) continue;

    const extension = path.extname(entry.name).toLowerCase();
    const relativePath = path.relative(repoRoot, fullPath) || entry.name;
    if (licenseNamePattern.test(entry.name)) {
      stats.licenses.push(relativePath);
    }
    if (contentExtensions.has(extension) || (extension === '.json' && isContentJson(relativePath))) {
      stats.files.push(relativePath);
      stats.byExtension[extension] = (stats.byExtension[extension] || 0) + 1;
    }
  }
};

const auditRoots = async (roots) => {
  const results = [];
  for (const root of roots) {
    const absolute = path.resolve(repoRoot, root);
    const stats = {
      root,
      files: [],
      licenses: [],
      byExtension: {},
    };
    await walk(absolute, stats);
    stats.files.sort();
    stats.licenses.sort();
    results.push(stats);
  }
  return results;
};

const renderCatalog = (catalog) => [
  '| ID | Status | Policy | License | Source | Notes |',
  '| --- | --- | --- | --- | --- | --- |',
  ...catalog.entries.map((entry) => [
    entry.id,
    entry.status,
    entry.copyPolicy,
    entry.license,
    entry.source?.url || entry.source?.path || '',
    String(entry.notes || '').replace(/\|/g, '\\|'),
  ].map((value) => String(value).replace(/\n/g, ' ')).join(' | ')).map((line) => `| ${line} |`),
].join('\n');

const renderScans = (scans) => scans.map((scan) => {
  const extensions = Object.entries(scan.byExtension)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([extension, count]) => `  - \`${extension}\`: ${count}`)
    .join('\n') || '  - none';
  const licenseFiles = scan.licenses.slice(0, 20)
    .map((file) => `  - \`${file}\``)
    .join('\n') || '  - none found';
  const samples = scan.files.slice(0, 40)
    .map((file) => `  - \`${file}\``)
    .join('\n') || '  - none found';
  const truncated = scan.files.length > 40
    ? `\n  - ... ${scan.files.length - 40} more`
    : '';

  return `## Local Scan: \`${scan.root}\`

Content-like files: ${scan.files.length}

By extension:

${extensions}

License/provenance files:

${licenseFiles}

Sample content files:

${samples}${truncated}
`;
}).join('\n');

const main = async () => {
  const catalog = await readJson(catalogPath);
  const roots = scanRoots.length ? scanRoots : ['.'];
  const scans = await auditRoots(roots);
  const markdown = `# Third-Party Content Audit

Generated by \`node tools/audit-milkrust-content-sources.mjs\`.

## Policy Summary

MilkRust can load old MilkDrop-family content. Clearly licensed content lives under \`content/third-party\`; aggressive public community imports with unclear/no explicit licenses live under \`content/community-unlicensed\` and are excluded from default builds unless a distribution explicitly opts in.

## Catalog

${renderCatalog(catalog)}

${renderScans(scans)}
`;

  if (write) {
    await fs.writeFile(outputPath, markdown);
  }
  process.stdout.write(markdown);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
